"""Export the retention task for the reference implementation.

A control run: the same rows, the same tables, the same anchors and the same
labels, handed to ``~/relational-transformer`` instead of to this engine. If
the two disagree by a lot, one of them is wrong about something.

Writes a DuckDB database plus label parquet files, and a ``config.py`` for the
reference's three-step BYOD flow (``1_data_prep`` / ``2_task_prep`` /
``3_predict``). The split is reproduced from :mod:`gh.retention` by calling the
same functions with the same seeds, so the entity sets are identical row for
row rather than merely drawn the same way.

Two deliberate differences, both provably neutral:

  * timestamps are written naive UTC. Every value in this feed is UTC already,
    so this drops a label rather than information, and it keeps the reference's
    parquet and Rust paths off a timezone-aware code path they need not take.
  * ``came_back`` appears only in the label files, never in a table. That is
    the same arrangement this engine runs under, where the column is declared
    in the schema and served on no row.

    python -m gh.export_ref --train 900 --test 600
"""
from __future__ import annotations

import argparse
import warnings
from pathlib import Path

from . import retention as R

REF = Path.home() / "relational-transformer"
OUT = REF / "examples" / "inference" / "out"
DB_NAME = "gh-retention"
TASK_NAME = "account-returns"

# Which of the reference's manifest entries each served table becomes. Same
# primary keys, same foreign keys, same time columns as the schema in
# gh.retention -- this is that schema restated in the reference's vocabulary.
TABLES = {
    "actors": {"pkey": "actor_login"},
    "orgs": {"pkey": "org_login"},
    "repos": {"pkey": "repo_name", "fkeys": {"org_login": "orgs"}},
    "events": {"pkey": "event_id", "time_col": "ts",
               "fkeys": {"repo_name": "repos", "actor_login": "actors"}},
    "repo_people": {"pkey": "rp_key", "time_col": "ts",
                    "fkeys": {"repo_name": "repos", "actor_login": "actors"}},
    "repo_days": {"pkey": "rd_key", "time_col": "ts",
                  "fkeys": {"repo_name": "repos"}},
    "pull_requests": {"pkey": "pull_key", "time_col": "ts",
                      "fkeys": {"repo_name": "repos",
                                "actor_login": "actors"}},
    "pull_events": {"pkey": "pe_id", "time_col": "ts",
                    "fkeys": {"pull_key": "pull_requests",
                              "repo_name": "repos",
                              "actor_login": "actors"}},
    "reviews": {"pkey": "review_id", "time_col": "ts",
                "fkeys": {"pull_key": "pull_requests", "repo_name": "repos",
                          "actor_login": "actors"}},
    "issues": {"pkey": "issue_key", "time_col": "ts",
               "fkeys": {"repo_name": "repos", "actor_login": "actors"}},
    "comments": {"pkey": "comment_id", "time_col": "ts",
                 "fkeys": {"issue_key": "issues", "repo_name": "repos",
                           "actor_login": "actors"}},
}

CONFIG = '''"""Generated by relativedb's benchmarks/gh/export_ref.py -- do not hand-edit.

The GH Archive retention task, in the reference implementation's own format,
for a control run against the same task scored by relativedb.
"""
from pathlib import Path

_HERE = Path(__file__).resolve().parent
DATA_DIR = _HERE / "out"
DB_NAME = {db!r}
SQL_URI = str(DATA_DIR / "{db}.duckdb")
TABLES = {tables!r}
TASK = {{
    "name": {task!r},
    "entity_table": "actors",
    "entity_col": "actor_login",
    "time_col": "ts",
    "target_col": "came_back",
    "task_type": "binary_classification",
    "splits": {{
        "train": str(DATA_DIR / "labels" / "train.parquet"),
        "test": str(DATA_DIR / "labels" / "test.parquet"),
    }},
}}
CHECKPOINT = "stanford-star/rt-j/classification"
'''


def split(g, args):
    """The identical train/test rows gh.retention.run would use."""
    arr, y, ent, lo, hi = R.entities(g)
    fr = R.frames(g, R.scope_of(g, args.min_actors))
    ent = ent[ent.repo_name.isin(set(fr["repos"].repo_name))]
    cut = ent.t0.quantile(args.split)
    early, late = ent[ent.t0 <= cut], ent[ent.t0 > cut]
    tr = early.sample(n=min(args.train, len(early)),
                      random_state=1).sort_values("t0")
    te = late.sample(n=min(args.test, len(late)),
                     random_state=2).sort_values("t0")
    return fr, tr, te


def main():
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--min-actors", type=int, default=2)
    ap.add_argument("--train", type=int, default=900)
    ap.add_argument("--test", type=int, default=600)
    ap.add_argument("--split", type=float, default=0.6)
    args = ap.parse_args()
    warnings.filterwarnings("ignore")

    import duckdb

    g = R.load()
    fr, tr, te = split(g, args)
    assert set(fr) == set(TABLES), (set(fr) ^ set(TABLES))

    OUT.mkdir(parents=True, exist_ok=True)
    (OUT / "labels").mkdir(exist_ok=True)
    db = OUT / f"{DB_NAME}.duckdb"
    db.unlink(missing_ok=True)
    con = duckdb.connect(str(db))
    try:
        for name, frame in fr.items():
            f = frame.copy()
            for c in f.columns:
                if str(f[c].dtype).startswith("datetime64") and f[c].dt.tz:
                    f[c] = f[c].dt.tz_localize(None)
            con.register("_f", f)
            con.execute(f'CREATE TABLE "{name}" AS SELECT * FROM _f')
            con.unregister("_f")
            print(f"  {name:15} {len(f):>9,} rows  "
                  f"[{', '.join(f.columns)}]", flush=True)
    finally:
        con.close()

    for tag, frame in (("train", tr), ("test", te)):
        lab = frame[["actor_login", "t0", "y"]].rename(
            columns={"t0": "ts", "y": "came_back"}).copy()
        lab["ts"] = lab.ts.dt.tz_localize(None)
        lab["came_back"] = lab.came_back.astype("int64")
        lab.to_parquet(OUT / "labels" / f"{tag}.parquet", index=False)
        print(f"  {tag+' labels':15} {len(lab):>9,} rows  "
              f"{lab.came_back.mean():.1%} positive  "
              f"{lab.ts.min()} .. {lab.ts.max()}", flush=True)

    overlap = set(tr.actor_login) & set(te.actor_login)
    if overlap:
        print(f"\nnote: {len(overlap)} account(s) appear in both splits at "
              f"different anchors -- an account can wake more than once, and "
              f"each wake is its own row. The same is true of the relativedb "
              f"run this is controlling against, so the two remain comparable.")

    cfg = REF / "examples" / "inference" / "config.py"
    cfg.write_text(CONFIG.format(db=DB_NAME, task=TASK_NAME, tables=TABLES))
    print(f"\nwrote {db}\nwrote {cfg}")
    print(f"\nnext, from {REF}:\n"
          f"  .venv/bin/python examples/inference/1_data_prep.py\n"
          f"  .venv/bin/python examples/inference/2_task_prep.py\n"
          f"  .venv/bin/python examples/inference/3_predict.py --device cpu")


if __name__ == "__main__":
    main()
